// Get CPU image path
export const getCPUImage = (model) => {
  if (!model) return null;

  // Try exact match first
  if (cpuImageMap[model]) {
    return `/src/assets/Images/CPU/${cpuImageMap[model]}`;
  }

  // Try partial match
  for (const [key, value] of Object.entries(cpuImageMap)) {
    if (model.includes(key)) {
      return `/src/assets/Images/CPU/${value}`;
    }
  }

  return null;
};

// Get GPU image path
export const getGPUImage = (manufacturer, model) => {
  if (!manufacturer || !model) return null;

  const fullName = `${manufacturer} ${model}`;

  // Try exact match first
  if (gpuImageMap[fullName]) {
    return `/src/assets/Images/GPU/${gpuImageMap[fullName]}`;
  }

  // Try manufacturer + simplified model
  const simplifiedModel = model.replace("GeForce ", "").replace("Radeon ", "");
  const simplifiedName = `${manufacturer} ${simplifiedModel}`;
  if (gpuImageMap[simplifiedName]) {
    return `/src/assets/Images/GPU/${gpuImageMap[simplifiedName]}`;
  }

  // Try partial match
  for (const [key, value] of Object.entries(gpuImageMap)) {
    if (
      fullName.includes(key) ||
      key.includes(manufacturer && simplifiedModel)
    ) {
      return `/src/assets/Images/GPU/${value}`;
    }
  }

  return null;
};
